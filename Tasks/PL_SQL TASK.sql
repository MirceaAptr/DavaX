CREATE OR REPLACE PROCEDURE adjust_salaries_by_commission
IS
    CURSOR c_employees IS
        SELECT employee_id,
               salary,
               commission_pct
        FROM demo_employees
        FOR UPDATE OF salary;

    v_new_salary demo_employees.salary%TYPE;
    v_increase   demo_employees.salary%TYPE;
    v_updated    PLS_INTEGER := 0;

BEGIN
    debug_util.log(
        p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
        p_line        => 1,
        p_message     => 'Procedure started.',
        p_level       => 'INFO'
    );

    FOR employee_rec IN c_employees LOOP

        debug_util.log(
            p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
            p_line        => 2,
            p_message     => 'Reading employee ID ' ||
                             employee_rec.employee_id || '.',
            p_level       => 'DEBUG'
        );

        debug_util.log_variable(
            p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
            p_line        => 3,
            p_name        => 'Current salary',
            p_value       => TO_CHAR(employee_rec.salary)
        );

        debug_util.log_variable(
            p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
            p_line        => 4,
            p_name        => 'Commission percentage',
            p_value       => TO_CHAR(employee_rec.commission_pct)
        );

        IF employee_rec.commission_pct IS NOT NULL THEN

            v_increase :=
                employee_rec.salary * employee_rec.commission_pct;

            v_new_salary :=
                employee_rec.salary + v_increase;

            debug_util.log(
                p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
                p_line        => 5,
                p_message     => 'Commission exists. Salary increased by ' ||
                                 TO_CHAR(v_increase) || '.',
                p_level       => 'INFO'
            );

        ELSE
            v_increase :=
                employee_rec.salary * 0.02;

            v_new_salary :=
                employee_rec.salary + v_increase;

            debug_util.log(
                p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
                p_line        => 6,
                p_message     => 'Commission is NULL. Default 2% increase ' ||
                                 'applied.',
                p_level       => 'INFO'
            );
        END IF;

        debug_util.log_variable(
            p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
            p_line        => 7,
            p_name        => 'Calculated new salary',
            p_value       => TO_CHAR(v_new_salary)
        );

        UPDATE demo_employees
        SET salary = ROUND(v_new_salary, 2)
        WHERE CURRENT OF c_employees;

        v_updated := v_updated + SQL%ROWCOUNT;

        debug_util.log(
            p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
            p_line        => 8,
            p_message     => 'Salary updated for employee ID ' ||
                             employee_rec.employee_id || '.',
            p_level       => 'DEBUG'
        );

    END LOOP;

    COMMIT;

    debug_util.log(
        p_module_name => 'ADJUST_SALARIES_BY_COMMISSION',
        p_line        => 9,
        p_message     => 'Procedure completed successfully. Employees updated: ' ||
                         v_updated || '.',
        p_level       => 'INFO'
    );

    DBMS_OUTPUT.PUT_LINE(
        'Salary adjustment completed. Employees updated: ' || v_updated
    );

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;

        debug_util.log_error(
            p_proc => 'ADJUST_SALARIES_BY_COMMISSION',
            p_line => 10,
            p_err  => 'SQLCODE=' || SQLCODE || '; SQLERRM=' || SQLERRM ||
                      '; BACKTRACE=' || DBMS_UTILITY.FORMAT_ERROR_BACKTRACE
        );

        RAISE;
END adjust_salaries_by_commission;
/

/*
Explanation:
The procedure reads every employee using an explicit cursor.

For each employee:
    1. It logs that the employee was read.
    2. It logs the current salary and commission percentage.
    3. If COMMISSION_PCT is not NULL, it increases the salary proportionally.
    4. If COMMISSION_PCT is NULL, it increases the salary by 2%.
    5. It updates the current row.
    6. It records the update in DEBUG_LOG.

The salary is rounded to two decimal places.

If the procedure succeeds, it commits all salary updates.
If an error occurs, it rolls back all salary updates, records the error, and
raises the exception again.
*/

