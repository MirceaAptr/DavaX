CREATE OR REPLACE PACKAGE debug_util IS

    g_debug_mode BOOLEAN := FALSE;

    PROCEDURE enable_debug;

    PROCEDURE disable_debug;

    PROCEDURE log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_message     IN VARCHAR2,
        p_level       IN VARCHAR2 DEFAULT 'INFO'
    );

    PROCEDURE log_variable(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_name        IN VARCHAR2,
        p_value       IN VARCHAR2
    );

    PROCEDURE log_error(
        p_proc IN VARCHAR2,
        p_line IN NUMBER,
        p_err  IN VARCHAR2
    );

END debug_util;
/

CREATE OR REPLACE PACKAGE BODY debug_util IS

    PROCEDURE write_log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_message     IN VARCHAR2,
        p_level       IN VARCHAR2
    )
    IS
        PRAGMA AUTONOMOUS_TRANSACTION;
        v_level debug_log.log_level%TYPE;
    BEGIN
        v_level := UPPER(NVL(TRIM(p_level), 'INFO'));

        IF v_level NOT IN ('DEBUG', 'INFO', 'WARN', 'ERROR') THEN
            v_level := 'INFO';
        END IF;

        INSERT INTO debug_log (
            module_name,
            line_no,
            log_level,
            log_message
        )
        VALUES (
            SUBSTR(p_module_name, 1, 100),
            p_line,
            v_level,
            SUBSTR(p_message, 1, 4000)
        );

        COMMIT;

    EXCEPTION
        WHEN OTHERS THEN
            ROLLBACK;
    END write_log;


    PROCEDURE enable_debug IS
    BEGIN
        g_debug_mode := TRUE;

        write_log(
            p_module_name => 'DEBUG_UTIL',
            p_line        => 1,
            p_message     => 'Debug mode enabled.',
            p_level       => 'INFO'
        );
    END enable_debug;


    PROCEDURE disable_debug IS
    BEGIN
        write_log(
            p_module_name => 'DEBUG_UTIL',
            p_line        => 2,
            p_message     => 'Debug mode disabled.',
            p_level       => 'INFO'
        );

        g_debug_mode := FALSE;
    END disable_debug;


    PROCEDURE log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_message     IN VARCHAR2,
        p_level       IN VARCHAR2 DEFAULT 'INFO'
    )
    IS
    BEGIN
        IF g_debug_mode THEN
            write_log(
                p_module_name => p_module_name,
                p_line        => p_line,
                p_message     => p_message,
                p_level       => p_level
            );
        END IF;
    END log;


    PROCEDURE log_variable(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_name        IN VARCHAR2,
        p_value       IN VARCHAR2
    )
    IS
    BEGIN
        IF g_debug_mode THEN
            write_log(
                p_module_name => p_module_name,
                p_line        => p_line,
                p_message     => p_name || ' = ' || NVL(p_value, 'NULL'),
                p_level       => 'DEBUG'
            );
        END IF;
    END log_variable;


    PROCEDURE log_error(
        p_proc IN VARCHAR2,
        p_line IN NUMBER,
        p_err  IN VARCHAR2
    )
    IS
    BEGIN
        write_log(
            p_module_name => p_proc,
            p_line        => p_line,
            p_message     => p_err,
            p_level       => 'ERROR'
        );
    END log_error;

END debug_util;
/


