CREATE TABLE employees (
    employee_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    first_name VARCHAR2(50) NOT NULL,
    last_name VARCHAR2(50) NOT NULL,
    email VARCHAR2(100) NOT NULL,
    job_title VARCHAR2(100),
    employee_status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CONSTRAINT pk_employees PRIMARY KEY (employee_id),
    CONSTRAINT uq_employees_email UNIQUE (email),
    CONSTRAINT ck_employees_status
        CHECK (employee_status IN ('ACTIVE', 'INACTIVE'))
);

CREATE TABLE projects (
    project_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    project_name VARCHAR2(100) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE,
    project_status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CONSTRAINT pk_projects PRIMARY KEY (project_id),
    CONSTRAINT uq_projects_name UNIQUE (project_name),
    CONSTRAINT ck_projects_dates
        CHECK (end_date IS NULL OR end_date >= start_date),
    CONSTRAINT ck_projects_status
        CHECK (project_status IN ('ACTIVE', 'COMPLETED', 'CANCELLED'))
);

CREATE TABLE tasks (
    task_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    project_id NUMBER NOT NULL,
    task_name VARCHAR2(150) NOT NULL,
    task_status VARCHAR2(20) DEFAULT 'OPEN' NOT NULL,
    estimated_hours NUMBER(6,2),
    CONSTRAINT pk_tasks PRIMARY KEY (task_id),
    CONSTRAINT fk_tasks_project
        FOREIGN KEY (project_id) REFERENCES projects(project_id),
    CONSTRAINT ck_tasks_status
        CHECK (task_status IN ('OPEN', 'IN_PROGRESS', 'COMPLETED')),
    CONSTRAINT ck_tasks_hours
        CHECK (estimated_hours IS NULL OR estimated_hours >= 0)
);

CREATE TABLE timesheets (
    timesheet_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    employee_id NUMBER NOT NULL,
    period_start DATE NOT NULL,
    period_end DATE NOT NULL,
    timesheet_status VARCHAR2(20) DEFAULT 'DRAFT' NOT NULL,
    CONSTRAINT pk_timesheets PRIMARY KEY (timesheet_id),
    CONSTRAINT fk_timesheets_employee
        FOREIGN KEY (employee_id) REFERENCES employees(employee_id),
    CONSTRAINT uq_timesheets_employee_period
        UNIQUE (employee_id, period_start, period_end),
    CONSTRAINT ck_timesheets_dates
        CHECK (period_end >= period_start),
    CONSTRAINT ck_timesheets_status
        CHECK (timesheet_status IN ('DRAFT', 'SUBMITTED', 'APPROVED', 'REJECTED'))
);

CREATE TABLE timesheet_entries (
    entry_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    timesheet_id NUMBER NOT NULL,
    task_id NUMBER NOT NULL,
    work_date DATE NOT NULL,
    hours_worked NUMBER(5,2) NOT NULL,
    work_description VARCHAR2(500),
    entry_details CLOB,
    CONSTRAINT pk_timesheet_entries PRIMARY KEY (entry_id),
    CONSTRAINT fk_entries_timesheet
        FOREIGN KEY (timesheet_id) REFERENCES timesheets(timesheet_id),
    CONSTRAINT fk_entries_task
        FOREIGN KEY (task_id) REFERENCES tasks(task_id),
    CONSTRAINT ck_entries_hours
        CHECK (hours_worked > 0 AND hours_worked <= 24),
    CONSTRAINT ck_entries_json
        CHECK (entry_details IS JSON)
);

CREATE INDEX idx_employees_last_name ON employees(last_name);
CREATE INDEX idx_projects_status ON projects(project_status);
CREATE INDEX idx_tasks_name ON tasks(task_name);
CREATE INDEX idx_timesheets_period_start ON timesheets(period_start);
CREATE INDEX idx_entries_work_date ON timesheet_entries(work_date);


