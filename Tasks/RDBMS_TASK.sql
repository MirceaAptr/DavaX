CREATE OR REPLACE VIEW vw_employee_hours AS
SELECT
    e.employee_id,
    e.first_name,
    e.last_name,
    NVL(SUM(te.hours_worked), 0) AS total_hours
FROM employees e
LEFT JOIN timesheets ts
    ON ts.employee_id = e.employee_id
LEFT JOIN timesheet_entries te
    ON te.timesheet_id = ts.timesheet_id
GROUP BY
    e.employee_id,
    e.first_name,
    e.last_name;

SELECT * FROM vw_employee_hours;

CREATE MATERIALIZED VIEW mv_project_hours
BUILD IMMEDIATE
REFRESH COMPLETE ON DEMAND
AS
SELECT
    p.project_id,
    p.project_name,
    NVL(SUM(te.hours_worked), 0) AS total_hours
FROM projects p
LEFT JOIN tasks t
    ON t.project_id = p.project_id
LEFT JOIN timesheet_entries te
    ON te.task_id = t.task_id
GROUP BY
    p.project_id,
    p.project_name;

SELECT * FROM mv_project_hours;

-- Displays all employees stored in the database.
SELECT
    employee_id,
    first_name,
    last_name,
    email,
    job_title,
    employee_status
FROM employees
ORDER BY employee_id;

-- Displays all projects and their current status.
SELECT
    project_id,
    project_name,
    start_date,
    end_date,
    project_status
FROM projects
ORDER BY project_id;

-- Calculates the total number of worked hours for each employee using GROUP BY.
SELECT
    e.employee_id,
    e.first_name,
    e.last_name,
    SUM(te.hours_worked) AS total_hours
FROM employees e
JOIN timesheets ts
    ON ts.employee_id = e.employee_id
JOIN timesheet_entries te
    ON te.timesheet_id = ts.timesheet_id
GROUP BY
    e.employee_id,
    e.first_name,
    e.last_name
ORDER BY total_hours DESC;

-- Displays all employees, including employees who do not have a timesheet, using LEFT JOIN.
SELECT
    e.employee_id,
    e.first_name,
    e.last_name,
    ts.timesheet_id,
    ts.period_start,
    ts.period_end,
    ts.timesheet_status
FROM employees e
LEFT JOIN timesheets ts
    ON ts.employee_id = e.employee_id
ORDER BY
    e.employee_id,
    ts.period_start;

-- Compares each entry with the average hours for the same project using an analytic function.
SELECT
    p.project_name,
    t.task_name,
    te.work_date,
    te.hours_worked,
    ROUND(
        AVG(te.hours_worked) OVER (PARTITION BY p.project_id),
        2
    ) AS average_project_hours
FROM timesheet_entries te
JOIN tasks t
    ON t.task_id = te.task_id
JOIN projects p
    ON p.project_id = t.project_id
ORDER BY
    p.project_name,
    te.work_date;


